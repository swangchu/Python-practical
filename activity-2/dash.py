# Activity 2: dash.py
# -----------------------------------------------------------------
# Write a program using Turtle module to generate five dash lines
# starting 400 pixels from left of the center of the canvas and each dash to be
# 100 pixels and they are 100 pixels apath.
# ---------------------------------------------------------------------

from turtle import *
shape("turtle")

penup()
backward(400)
pendown()
forward(100)

penup()
forward(100)
pendown()
forward(100)

penup()
forward(100)
pendown()
forward(100)

penup()
forward(100)
pendown()
forward(100)

penup()
forward(100)
pendown()
forward(100)
